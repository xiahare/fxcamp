
#include <iostream>
#include <queue>

using namespace std;

vector<int> dirs = {1,-1};
struct record{
    int step;
    bool visited=false;
};

int main() {

    int n;
    cin >> n;
    while(n!=0){
        
        int a, b;
        cin >> a >> b;
        
        vector<int> ki(n+1,0);
        for(int i=1; i<=n ; i++){
            cin >> ki[i];
        }
        
        queue<int> q;
        vector<record> r(n+1,{-1,false});
        q.push(a);
        r[a]={0,true};
        
        bool achieved = false;
        while (!q.empty()) {
            int t = q.front();
            q.pop();
            if(t==b){
                // destination
                cout<<r[t].step<<endl;
                achieved=true;
                break;
                
            }
            
            for(auto dir: dirs){

                int next_floor = t + ki[t] * dir;
                int next_step = r[t].step + 1;

                if( next_floor>0 && next_floor<=n && !r[next_floor].visited ){
                    q.push(next_floor);
                    r[next_floor]={next_step,true};
                }
            }
        }
        
        // no way
        if(!achieved){
            cout << -1<<endl;
        }
        
        cin >> n;
        
    }

    return 0;
}

