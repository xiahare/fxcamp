#include <iostream>
#include <vector>
#include <map>
using namespace std;
int main() {
    int n,x;
    cin >> n >> x;
   
    vector<long> vec(n+1,0);
    map<long,long> m;
    long cnt = 0;
    
    m[0]=1;
    for(int i=1; i<n+1; i++){
        int a;
        cin >> a;
        vec[i] = vec[i-1] + a; // prefix
        long b = vec[i] - x;
        cnt = cnt + m[b];
        m[vec[i]]++;
    }
    
    cout << cnt;
    return 0;
}

